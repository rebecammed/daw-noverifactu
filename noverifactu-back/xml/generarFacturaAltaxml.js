import { create } from "xmlbuilder2";

export default function generarFacturaAltaXML(datos) {
  const doc = create({ version: "1.0", encoding: "UTF-8" })
    .ele("FacturaNoVerifactu", { xmlns: "https://noverifactu.local/esquema" })
    .ele("Cabecera")
    .ele("NIFEmisor")
    .txt(datos.cabecera.nifEmisor)
    .up()
    .ele("NIFReceptor")
    .txt(datos.cabecera.nifReceptor)
    .up()
    .ele("VersionSIF")
    .txt(datos.cabecera.versionSIF)
    .up()
    .up()
    .ele("DatosFactura")
    .ele("NumeroFacturaCompleto")
    .txt(datos.datosFactura.numeroFactura)
    .up()
    .ele("FechaHoraExpedicion")
    .txt(datos.datosFactura.fechaExpedicion)
    .up()
    .ele("TipoFactura")
    .txt(datos.datosFactura.tipoFactura)
    .up()
    .ele("ImporteTotalFactura")
    .txt(datos.datosFactura.importeTotal)
    .up()
    .up()
    .ele("DesgloseFiscal")
    .ele("Impuestos");
  datos.desgloseFiscal.impuestos.forEach((imp) => {
    doc
      .ele("Impuesto")
      .ele("BaseImponible")
      .txt(imp.base)
      .up()
      .ele("TipoImpositivo")
      .txt(imp.tipoImpositivo)
      .up()
      .ele("Cuota")
      .txt(imp.cuota)
      .up()
      .ele("Tipo")
      .txt(imp.tipoImpuesto)
      .up()
      .up();
  });
  doc
    .up()
    .up()
    .ele("Trazabilidad")
    .ele("NumRegistroAnterior")
    .txt(datos.trazabilidad.numRegAnt)
    .up()
    .ele("NumRegistroActual")
    .txt(datos.trazabilidad.numRegAct)
    .up()
    .ele("HashRegistroAnterior")
    .txt(datos.trazabilidad.hashAnterior)
    .up()
    .ele("HashRegistroPropio")
    .txt(datos.trazabilidad.hashPropio)
    .up()
    .ele("IdentificacionSIF")
    .ele("IdSoftware")
    .txt(datos.trazabilidad.identificacionSIF.idSw)
    .up()
    .ele("NIFDesarrollador")
    .txt(datos.trazabilidad.identificacionSIF.nifDev)
    .up()
    .ele("fechaDeclaracionResponsable")
    .txt(datos.trazabilidad.identificacionSIF.fechaDeclaracionResponsable)
    .up()
    .up()
    .up()
    .up();
  return doc.end({ prettyPrint: true });
}
